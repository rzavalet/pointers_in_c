#include <stdio.h>

int main()
{
  static char *s[] = {"ice", "green", "cone", "please"};
  static char **ptr[] = {s+3, s+2, s+1, s+1, s};
  char ***p = ptr;

  /*---------------------------
   * ++p           : points to &(ptr[1])
   * *++p          : is ptr[1] = s+2
   * **++pp        : is "cone"
   *---------------------------"*/
  printf("%s\n", **++p);

  /*---------------------------
   * ++p           : points to &(ptr[2]) = (ptr + 2)
   * *++p          : is ptr[2] = s+1
   * --*++p        : points to s
   * *--*++p       : is "ice\0"
   * *--*++p + 3   : is '\0'
   *---------------------------"*/
  printf("%s\n", *--*++p + 3);

  /*---------------------------
   * p[-2]         : is *(p - 2) = *(ptr + 2 - 2) = s + 3
   * *p[-2]        : is "please\0"
   * *p[-2] + 3    : is "ase\0"
   *---------------------------"*/
  printf("%s\n", *p[-2] + 3);

  /*---------------------------
   * p[-1]         : is *(p - 1) = *(ptr + 2 -1) = * (ptr + 1) = s + 2
   * p[-1][-1]     : is (s+2)[-1] = *(s + 2 - 1) = *(s + 1) = "green\0"
   * p[-1][-1] + 1 : is "reen\0"
   *---------------------------"*/
  printf("%s\n", p[-1][-1] + 1);

  return 0;
}
